    private void vymazReceptBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_vymazReceptBTActionPerformed
        String[] nastaveni = {"smazat","zrušit"};
        int s = JOptionPane.showOptionDialog(this, "Opravdu chcete vymazat recept " + nazvyReceptuList.getSelectedItem() + "?",
            "",JOptionPane.OK_CANCEL_OPTION,JOptionPane.QUESTION_MESSAGE, null,
            nastaveni, nastaveni[1]);
        if (s == JOptionPane.OK_OPTION) {
            Recept r = DB.nactiRecept(nazvyReceptuList.getSelectedItem());
            DB.vymazRecept(r);
            DB.vymazReceptZeSlozky(r);
            DB.nactiNazvy(nazvyReceptuList);
        }
    }//GEN-LAST:event_vymazReceptBTActionPerformed

