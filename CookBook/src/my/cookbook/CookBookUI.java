    private static String prevedText(String vstup) {
        return vstup = Normalizer.normalize(vstup, Normalizer.Form.NFKD).replaceAll("\\p{InCombiningDiacriticalMarks}+", "").replaceAll(" ", "").toLowerCase();        
    }
    private void hledejRecept() {
        ArrayList <String> nazvyIng = new ArrayList<>();
        if(!nazevIngTF1.getText().isEmpty()) {nazvyIng.add(prevedText(nazevIngTF1.getText()));}
        if(!nazevIngTF2.getText().isEmpty()) {nazvyIng.add(prevedText(nazevIngTF2.getText()));}
        if(!nazevIngTF3.getText().isEmpty()) {nazvyIng.add(prevedText(nazevIngTF3.getText()));}
        if(!nazevIngTF4.getText().isEmpty()) {nazvyIng.add(prevedText(nazevIngTF4.getText()));}
        if(!nazevIngTF5.getText().isEmpty()) {nazvyIng.add(prevedText(nazevIngTF5.getText()));}
        if(!nazevIngTF6.getText().isEmpty()) {nazvyIng.add(prevedText(nazevIngTF6.getText()));}
        if(!nazevIngTF7.getText().isEmpty()) {nazvyIng.add(prevedText(nazevIngTF7.getText()));}
        if(!nazevIngTF8.getText().isEmpty()) {nazvyIng.add(prevedText(nazevIngTF8.getText()));}
        if(!nazevIngTF9.getText().isEmpty()) {nazvyIng.add(prevedText(nazevIngTF9.getText()));}
        if(!nazevIngTF10.getText().isEmpty()) {nazvyIng.add(prevedText(nazevIngTF10.getText()));}             
        DB.hledejNazvyReceptu(nazvyReceptuList, nazevReceptuTF.getText(), nazvyIng);

    }
